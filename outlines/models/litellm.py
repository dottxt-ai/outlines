def from_litellm(
    model_name: str,
    async_client: bool = False,
    **kwargs: Any,
) -> Union[LiteLLM, AsyncLiteLLM]:
    """Create an Outlines `LiteLLM` or `AsyncLiteLLM` model instance.

    LiteLLM provides a unified interface to 100+ LLM providers. See
    https://docs.litellm.ai/docs/providers for supported providers
    and model name formats.

    Parameters
    ----------
    model_name
        The model identifier in LiteLLM format (e.g. ``"gpt-4o"``,
        ``"anthropic/claude-3-sonnet"``, ``"gemini/gemini-pro"``).
    async_client
        If ``True``, return an ``AsyncLiteLLM`` instance for use with
        ``async``/``await``.
    **kwargs
        Additional keyword arguments passed to ``litellm.completion()``
        or ``litellm.acompletion()`` on every call (e.g. ``api_key``,
        ``api_base``, ``timeout``).

    Returns
    -------
    Union[LiteLLM, AsyncLiteLLM]
        An Outlines `LiteLLM` or `AsyncLiteLLM` model instance.

    """
    if async_client:
        return AsyncLiteLLM(model_name, **kwargs)
    return LiteLLM(model_name, **kwargs)
