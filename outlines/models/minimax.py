def from_minimax(
    client: Union["OpenAIClient", "AsyncOpenAIClient"],
    model_name: Optional[str] = None,
) -> Union[MiniMax, AsyncMiniMax]:
    """Create an Outlines ``MiniMax`` model from an ``openai`` client.

    If you do not already have a client, the easiest way to get started is::

        import openai
        import outlines

        client = openai.OpenAI(
            api_key="YOUR_MINIMAX_API_KEY",
            base_url="https://api.minimax.io/v1",
        )
        model = outlines.from_minimax(client, "MiniMax-M2.5")

    Parameters
    ----------
    client
        An ``openai.OpenAI`` or ``openai.AsyncOpenAI`` instance whose
        ``base_url`` is set to the MiniMax endpoint.
    model_name
        The MiniMax model to use, e.g. ``"MiniMax-M2.5"`` or
        ``"MiniMax-M2.5-highspeed"``.

    Returns
    -------
    Union[MiniMax, AsyncMiniMax]
        An Outlines ``MiniMax`` or ``AsyncMiniMax`` model instance.

    """
    import openai

    if isinstance(client, openai.AsyncOpenAI):
        return AsyncMiniMax(client, model_name)
    elif isinstance(client, openai.OpenAI):
        return MiniMax(client, model_name)
    else:
        raise ValueError(
            "Invalid client type. The client must be an instance of "
            "`openai.OpenAI` or `openai.AsyncOpenAI`."
        )
